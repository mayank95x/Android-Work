public class LikeUtil
{
    private String name;
    private String username;

    public LikeUtil(String name, String username)
    {
        this.name = name;
        this.username = username;
    }

    public String getName()
    {
        return this.name;
    }
    public String getUsername()
    {
        return this.username;
    }
}
